int				ft_ustrlen(const int *c)
{
	int				count;
	int				i;

	i = -1;
	count = 0;
	while (c[++i] != '\0')
	{
		if (c[i] <= 255)
			count += 1;
		else
		{
			if (c[i] < 2048)
				count += 2;
			else
			{
				if (c[i] < 65536)
					count += 3;
				else
				{
					(c[i] < 2097152) ? count += 4 : 0;
				}
			}
		}
	}
	return (count);
}
