#include "libft.h"

void	ft_putnbr(int n)
{
	long int		i;
	long int		q;
	long int		n2;

	i = 1;
	if (n < 0)
	{
		ft_putchar('-');
		i = -1;
	}
	n2 = n * i;
	q = n2;
	i = 1;
	while (q /= 10)
		i *= 10;
	i *= 10;
	while (i /= 10)
		ft_putchar((n2 / i % 10) + '0');
}
